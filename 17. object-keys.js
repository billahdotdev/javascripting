// object-keys.js

// Define a variable named car that references an object with make, model, and year properties
const car = {
  make: 'Honda',
  model: 'Accord',
  year: 2020,
};

// Use Object.keys() to get an array of the car object's keys
const keys = Object.keys(car);

// Print the keys array to the terminal
console.log(keys);
