// for-loop.js

// Define a variable named total and set it to 0
let total = 0;

// Define a variable named limit and set it to 10
let limit = 10;

// Create a for loop with variable i starting at 0 and running while i < limit
for (let i = 0; i < limit; i++) {
  // Add i to total (accumulating the total)
  total += i;
}

// Print the total to the terminal
console.log(total);
