// function-arguments.js

// Define a function named math with three arguments: a, b, c
function math(a, b, c) {
  // Multiply the second and third arguments, then add the first argument to the result
  return a + b * c;
}

// Call the math function with 53, 61, and 67 as arguments and print the result
console.log(math(53, 61, 67));
